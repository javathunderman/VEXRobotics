task flywheel()
{
	////////////TOGGLE MODE////////////////


		if(flywheelon==0)
		{
			if (vexRT[Btn7D]==1) //Press to change to press and hold mode
			{

				if(vexRT[Btn7D]==0)//activates when button is pushed and then let go
				{
				shootmode = 1;
				}

			}



			if(vexRT[Btn8U]==1)//High Button
			{
				motor(Leftflywheel) = 80;
				motor(Rightflywheel) = 80;

				if(vexRT[Btn8U]==0)
				{
					flywheelon = 1;
				}

			}

			if(vexRT[Btn8D]==1)//low botton
			{
				motor(Leftflywheel) = 40;
				motor(Rightflywheel) = 40;


				if(vexRT[Btn8D]==0)
				{
					flywheelon = 1;
				}

			}

			if(vexRT[Btn8R]==1)//medium high button
			{

				motor(Leftflywheel) = 75;
				motor(Rightflywheel) = 75;


				if(vexRT[Btn8R]==0)
				{
					flywheelon = 1;
				}

			}

			if(vexRT[Btn8L]==1)//medium low button
			{
				motor(Leftflywheel) = 64;
				motor(Rightflywheel) = 64;


				if(vexRT[Btn8L]==0)
				{
					flywheelon = 1;
				}

			}
		}

	/////////////////////////////////TOGGLE MODE CONTINUED/////////////////////
	if(flywheelon==1)
		{

		if (vexRT[Btn7D]==1) //Press to change to press and hold mode
		{

				if(vexRT[Btn7D]==0)//activates when button is pushed and then let go
				{
				shootmode = 1;
				}

		}

		if(vexRT[Btn8R]==1)//any button pushed again turns off motors
		{

			motor(Leftflywheel) = 0;
			motor(Rightflywheel) = 0;


				if(vexRT[Btn8R]==0)
				{
				flywheelon = 0;
				}

		}
		if(vexRT[Btn8U]==1)
		{

			motor(Leftflywheel) = 0;
			motor(Rightflywheel) = 0;


				if(vexRT[Btn8U]==0)
				{
				flywheelon = 0;
				}

		}
		if(vexRT[Btn8L]==1)
		{

			motor(Leftflywheel) = 0;
			motor(Rightflywheel) = 0;



				if(vexRT[Btn8L]==0)
				{
				flywheelon = 0;
				}

		}
		if(vexRT[Btn8D]==1)
		{

			motor(Leftflywheel) = 0;
			motor(Rightflywheel) = 0;


				if(vexRT[Btn8D]==0)
				{
				flywheelon = 0;
				}

			}
	}
}



