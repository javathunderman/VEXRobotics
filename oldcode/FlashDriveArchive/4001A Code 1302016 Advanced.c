task notifications()
{

	if(shootmode==0)
	{
		turnLEDOn(RED);
		turnLEDOff(GREEN);

	}

	if(shootmode==1)
	{
		turnLEDOn(GREEN);
		turnLEDOff(RED);
	}
	if(feedmodeon==1)
	{
		turnLEDOn(YELLOW);
	}
	if(feedmodeon==0)
	{
		turnLEDOff(YELLOW);
	}

}





